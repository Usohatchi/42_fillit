#include "libft.h"

size_t	ft_getdigits(int n)
{
	size_t	l;

	if (n == 0)
		return (1);
	l = 1;
	while ((n / ft_power(10, l)) != 0 && l < 10)
		l++;
	if (n < 0)
		l++;
	return (l);
}
