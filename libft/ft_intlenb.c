#include "libft.h"

size_t	ft_intlenb(long nb)
{
	size_t	len;

	len = 1;
	if (nb < 0)
	{
		nb = -nb;
		len++;
	}
	while (nb >= 2)
	{
		len++;
		nb = nb / 2;
	}
	return (len);
}
